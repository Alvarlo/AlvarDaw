public class Ejercicio2 {
    public static void main(String[] args) {
        /*
        Diseñar un programa que muestre el producto de los 10 primeros números impares.
         */
        int impares = 1;

        for (int i = 1; i <= 10; i++) {


            System.out.println(impares*impares);

            impares+=2;
        }


    }
}
